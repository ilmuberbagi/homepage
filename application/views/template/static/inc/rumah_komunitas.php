<h3>Sobat Komunitas Ilmu Berbagi</h3>
<p>Ilmu Berbagi Competence Center atau yang disingkat <b>IBCC</b> merupakan Pusat Pengembangan Kompetensi Ilmu Berbagi Foundation yang terdiri dari beberapa layanan sebagai berikut:</p>
<ul>
	<li>Pengembangan Keahlian</li>
    <li>Lembaga Pelatihan Komputer</li>
    <li>Pusat Kegiatan Belajar Masyarakat</li>
	<li>Pusat Studi/Kajian Keilmiahan yang terdiri dari beberapa kluster</li>
	<li>Ilmu Teknologi dan Komputer</li>
	<li>Ilmu Sosial dan Pendidikan</li>
	<li>Ilmu Kesehatan dan Lingkungan</li>
</ul>
<p>Saat ini pelayanan IBCC dipusatkan di Rumah Komunitas Ilmu Berbagi yang berlokasi di Jalan Pandega Siwi no.14b, Kaliurang Km5,6 Sleman, Jogjakarta. Pelayanan pengembangan kompetensi ini diberikan secara gratis. Pengembangan kompetensi kepada masyarakat umum melalui mekanisme pembiayaan dari mitra sosial, kerjasama perusahaan, dan pemerintah.</p>
